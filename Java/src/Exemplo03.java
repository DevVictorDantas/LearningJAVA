public class Exemplo03 {
  public static void main(String[] args) {

    int num1, num2;

    num1 = 6;
    num2 = 6;

    System.out.println(num1++); // o incremento só acontece para proxima
    // instrução

    System.out.println(++num2); // o incremento ja acontece na instrução atual

  }
}
